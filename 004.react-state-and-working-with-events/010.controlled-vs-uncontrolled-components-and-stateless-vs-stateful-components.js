/*

    Controlled vs uncontrolled components and stateless vs stateful components

    Whenever we use two way binding we are controlling the component which means that the value which is used in the
    component is passed on from the parent component through props. Thus, the real logic resides in the parent
    component. The child just consumes it to render on the UI.

    Stateful Component: In React, a stateful component is a component that holds some state.
    Stateless Component: Stateless components, by contrast, have no state. They are also called as presentational or dumb
    components.

    Note that both types of components can use props.

 */