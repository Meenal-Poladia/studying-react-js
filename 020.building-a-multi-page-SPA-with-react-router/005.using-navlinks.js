/*

    Using NavLinks

    If we are using the Link component for all the urls, it becomes difficult to understand on which link we are, as it
    is not highlighted or doesnt stand out. So, react has a certain component in which we can highlight the current
    link.

    Navlink does all the same thing as the Link component like changing the url, listening to the click and
    also displaying the content based on the url. However, navlink adds an extra css class on the active item.
    We need to tell the Navlink which class to add. It tells the Navlink to render the class when the
    link is active.

    By doing this we can highlight the link of the page on which we are currently present.

 */